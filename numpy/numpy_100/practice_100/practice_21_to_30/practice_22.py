# 22. Normalize a 5x5 random matrix

import numpy as np

arr = np.random.random((5, 5))
print(arr)
# [[0.46476816 0.77792666 0.76107354 0.62408672 0.87935872]
#  [0.47878705 0.2045595  0.65150066 0.22341447 0.67683767]
#  [0.90687068 0.58372278 0.74394036 0.53083206 0.58971857]
#  [0.65020955 0.33243793 0.68385011 0.92940572 0.18892015]
#  [0.99338456 0.90713415 0.00902027 0.97955375 0.77547177]]

# 정규화하는 방법...?
# 정규화된 배열 (값을 0-1 사이로 변환)
arr_normalized = (arr - arr.min()) / (arr.max() - arr.min())
print(arr_normalized)